MAIN:	MOV DX,1000
	MOV AX,%1110
	MOV CX,1
	SYS 1  ;LEE FRASE
	PUSH 1000 ;INICIO
	CALL PONE0
	ADD SP,1
	
	PUSH DX
	CALL ATOI ; DEVUELVE EN AX
	MOV BX,AX	
	SLEN CX,[DX] ;SALTEAMOS NUM1
	ADD DX,CX 	
	MOV EX,[CX]
	SLEN CX,[DX] ;SALTEAMOS OPERANDO
	ADD DX,CX 
	PUSH DX
	CALL ATOI ; DEVUELVE EN AX

	CMP EX,'+'
	JZ SUMA
	CMP EX,'-'
	JZ RESTA
	CMP EX,'*'
	MULT
	CMP EX,'/'
	DIVI


SUMA:	ADD BX,AX
RESTA:	SUB BX,AX
MULT:	MUL BX,AX
DIVI:	DIV BX,AX	

PONE0:	PUSH BP
	MOV BP,SP
	PUSH CX
	MOV CX,[BP+2] ;INICIO	
SIGUE:	CMP [CX],0
	JZ FIN
	CMP [CX],32
	JZ CERO
	ADD CX,1
	JMP SIGUE

CERO:	MOV [CX],0	 
	ADD CX,1
	JMP SIGUE


FIN:	POP CX
	MOV SP,BP
	POP BP
	
