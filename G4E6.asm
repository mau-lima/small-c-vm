MAIN:	MOV DX,1000
	MOV CX,1
	MOV AX,%1110
	SYS 1 //LEEMOS TODA LA POLACA
	
	SLEN FX,[1000]
	ADD FX,1000 ; EN FX GUARDO LA POS DEL /0

	PUSH 1000 ;INICIO
	CALL PONE0

	PUSH 1000
	PUSH FX
	CALL POLACA	;DEVUELVE RES EN AX
	ADD SP,2
	STOP
	
POLACA:	PUSH BP
	MOV BP,SP
	PUSH CX
	
	MOV CX,[BP+3] ;INICIO
	MOV DX,[BP+2] ;FIN

LOOP:	CMP CX,DX
	JZ FINPOLACA
	
	CMP EX,'+'
	JZ SUMA
	CMP EX,'-'
	JZ RESTA
	CMP EX,'*'
	MULT
	CMP EX,'/'
	DIVI
	
	PUSH [CX]
	CALL ATOI; EN AX
	ADD SP,1
	PUSH AX
	ADD CX,1
	JMP LOOP


SUMA:	POP AX
	POP BX
	ADD BX,AX
	PUSH BX
	ADD CX,1
	JMP LOOP

RESTA:	POP AX
	POP BX
	SUB BX,AX
	PUSH BX
	ADD CX,1
	JMP LOOP

MULT:	POP AX
	POP BX
	MUL BX,AX
	PUSH BX
	ADD CX,1
	JMP LOOP

DIVI:	POP AX
	POP BX
	DIV BX,AX
	PUSH BX
	ADD CX,1
	JMP LOOP

FINPOLCA: POP AX
	POP CX
	MOV SP,BP
	POP BP
	RET








PONE0:	PUSH BP
	MOV BP,SP
	PUSH CX
	MOV CX,[BP+2] ;INICIO	
SIGUE:	CMP [CX],0
	JZ FIN
	CMP [CX],32
	JZ CERO
	ADD CX,1
	JMP SIGUE

CERO:	MOV [CX],0	 
	ADD CX,1
	JMP SIGUE


FIN:	POP CX
	MOV SP,BP
	POP BP
	
	
	